#!/usr/bin/python3

saldo = 500
print(saldo)

saldo = 200
print(saldo)

# atribuição com adição
saldo += 10
print(saldo)

# atribuição com subtração
saldo -= 5
print(saldo)

# atribuição com divisão
saldo /= 2
print(saldo)

# atribuição com divisão inteira
saldo //= 2
print(saldo)

# atribuição com multiplicação
saldo *= 10
print(saldo)

# atribuição com exponenciação
saldo **= 2
print(saldo)
